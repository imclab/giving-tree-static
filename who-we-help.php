        <h2>Terms &amp; Conditions</h2>
        
        <h5>501 c (3) Status</h5>
        <p>a. Our Board of Directors has deemed it necessary to verify your 501-c (3) status. This assures us that the services we provide will reach the neediest children in our community. Proof of your status may be mailed or faxed to our office, and must be received before participating.  Government run agencies are exempt from this requirement.</p>
        
        <h5>Gift Pick Up Information</h5>
        <p>a. Your agency will be assigned a specific pick up date after you've entered your wishes.  We will email you the details, so please make sure the contact information you list on your registration form is correct.  While the date you are scheduled to pick up may change from year to year, the "pick up period" remains the same; between December 19th and December 23rd.  No exceptions, so please do not plan on receiving your gifts before 12/19.  Once your date is scheduled, you may choose to pick up your gifts later, but gifts cannot be picked up any earlier; as they simply will not be ready.</p>

        <p>b. Some agencies close for the Holiday season.  We understand this. However, <strong>we cannot get your gifts to you any earlier than your scheduled pickup time</strong>. If you know ahead of time that these gifts will not get to your clients in time for the Holidays, please start making alternate arrangements now.  Rally some volunteers to help you distribute, <strong>these children are worth it!</strong></p>

        <p>c. Your pickup date will be determined based on when you complete your wish list.  Earlier completed entry equals earlier pickup date!</p>

        <p>d. If you are unable to come to the warehouse to pick up your gifts, please make other arrangements to have them picked up.  Gifts left in the warehouse after the program ends will be given to other charities. </p>

        <h5>Gift Appropriateness</h5>
        <p>a. Gifts should cost no more than $30.  This is a strict requirement; if we receive wishes that are priced over this limit, we will edit the wish before printing it on a wish card.  Some children belong to the same household, so equality of gift value is important.</p>

        <p>b. Items such as brand name products, expensive electronics, weapons or designer items cannot be accepted.  This includes wishes such as "iPods", “Nike shoes”, “pocket knives”, "501 Jeans" or "Nintendo/XBOX" games.</p>

        <p>c. Please take great care when entering your wishes, and make sure each wish is age and gender appropriate.  For example, cards cannot be printed for a 16-year-old girl  asking for a "Tonka Truck."  The more specific the request, the more likely the child will get exactly what he/she wishes for.</p>

        <p>d. Please check for gift availability before entering.  If our donors cannot find the gift, it will be hard to fulfill the child’s wish.</p>

        <p>e. Once again, please be specific with the wishes. Wish lists requesting 50 of the same item will be returned for proper completion.</p>
