      <section id="faq">
        <h2>Agency FAQs</h2>
        <ul style="list-style:none;">
          <li>
            <h6>1. Does my agency qualify for the Holiday program?</h6>
            <p>The Family Giving Tree offers its Holiday Wish and Back to School Drives to Non-Profit organizations and schools throughout the Bay Area.  If your organization assists very needy children and you have obtained your 501(c)3, you may qualify.</p>  
          </li>
          <li>
            <h6>2. How can my organization apply to receive gifts?</h6>
            <p>A complete outline of the steps you need to take can be found here (link to application process).  We will need a copy of your 501(c)3, general information about your organization and complete contact information to get started. Once approved, you will be invited to register for our Holiday Wish Drive or Back to School Drive.</p>
          </li>
          <li>
            <h6>3. Once I register, what is the next step?</h6>
            <p>After you have completed the application process and registered your organization, agencies and schools receiving backpacks through our Back to School Drive are not required to provide anything else.  Agencies registered to receive gifts through our Holiday Wish Drive will receive instructions on how to "enter wishes." You must collect your client’s wishes and enter them into an online form before we can print them out for our donors.  These wishes are specific to your organization and must be appropriate and acceptable for printing onto wish cards.</p>
          </li>
          <li>
            <h6>4. Do I have to pay for these gift requests?</h6>
            <p>We DO NOT charge our registered organizations or recipients a fee to receive gifts or backpacks. We may, however, charge a penalty for agencies that enter their Holiday Wish Drive wishes late.</p>
          </li>
          <li>
            <h6>5. When can I pick up the gifts or backpacks?</h6>
            <p>Your agency will be assigned a specific pick up date shortly after you've registered and entered your wishes. We will email you the details, so please make sure the contact information you list on your registration form is correct. While the date you are scheduled to pick up may change from year to year, the "pick up period" remains the same.  All agencies and schools registered for the Back to School Drive will be assigned a pick up date between August 10th and August 20th. All agencies registered for the Holiday Wish Drive will be assigned a pick up date between December 19th and December 22nd.  We cannot make ANY exception to this, so please do not plan on receiving your donations outside of these dates. Donations cannot be picked up earlier than your scheduled date and time; as they simply will not be ready.</p>
          </li>
          <li>
            <h5>6. I can't make it to the warehouse to get my gifts or backpacks, what should I do?</h5>
            <p>We do not have the staff to deliver gifts or backpacks to your organization during in August or December.  If you cannot come to the warehouse to pick up your donations, please make other arrangements to have them picked up.  Donations left in the warehouse after the program ends will be given to organizations that are able to help those in need.</p>
          </li>
          
          <h2>Holiday Wish Drive specific questions </h2>
          
          <li>
            <h5>7. Is there a limit to how much the wish can cost?</h5>
            <p>Yes. We typically require all wishes to cost no more than $30. If we receive wishes that are priced over this limit, we may edit the wish before printing it on a wish card.</p>
          </li>
          <li>
            <h5>8. What type of wish would be considered "unacceptable"?</h5>
            <p>Items such as brand name products, weapons or designer items are unacceptable.  We cannot allow wishes such as "iPods", "Nike" shoes, "501 Jeans" or "Nintendo" games to be printed onto wish cards.  Please take great care when entering your wishes, and make sure each wish is age appropriate and gender appropriate. The more specific the requests, the more likely we will be able to get exactly what the child wishes for. For advice on entering wishes, check out our resource about collecting great wishes (link to collecting great wishes page).</p>
          </li>
          <li>
            <h5>9. Can I submit wishes after the deadline?</h5>
            <p>Once you submit wishes to us, we have a limited time to edit, print and send them out.  We are committed to getting the wish cards out to our donors and host companies by the second week of November. For this reason, we cannot extended the deadline, but we need to know that you have not dropped out if the program, so please let us know if you cannot make the deadline for entering wishes. A penalty fee may be added for late wishes.</p>
          </li>
          <li>
            <h5>10. Are these wishes guaranteed to be fulfilled?</h5>
            <p>If you submit your wishes by the posted deadline, we can guarantee that you will receive your requested number of gifts. We cannot guarantee that you will receive exactly what was asked for, but no child that you have signed up will do without.  If you miss the deadline for entering wishes, you will be placed on the "waiting list" and may incur a penalty fee. We cannot guarantee gifts to agencies on the waiting list, so please do not delay!</p>
          </li>
          <li>
            <h5>11. I've been put on the "waiting list", what does that mean?</h5>
            <p>This means that you have not completed the application/wish entering process by the posted deadline.  Because we are not able to receive, edit and print your wishes in time to send to our donors, we cannot guarantee your gifts. You will be contacted with more information.</p>
          </li>
          <li>
            <h5>12. Will the gifts come wrapped or unwrapped?</h5>
            <p>This is entirely up to you. We have over 6,000 volunteers working at our warehouse each year.  They are more than willing to wrap your gifts. However, if you prefer to receive your gifts unwrapped, please make sure you indicate your choice when registering.</p>
          </li>
          <li>
            <h5>13. My agency or school is closed during the Back to School Drive or Holiday Wish Drive, can I still participate?</h5>
            <p>Yes.  Some agencies shut down for the summer and holiday season.  We understand this, however we cannot get your donations to you any earlier than your scheduled pickup time.  This means you may have to put forth an extra effort to get these wishes to your clients.  We've had agencies open their doors for one night to hold a Holiday party for their clients, some have gathered at their local community center and other agencies have gone above and beyond to deliver each gift or backpack to their clients home.  If you know ahead of time that these items will not get to your clients in time for the school or the holidays, please start making alternate arrangements now.  Rally some volunteers to help you distribute, these children are worth it!</p>
          </li>
        </ul>
      </section>
      
